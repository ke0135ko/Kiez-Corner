    <h4>Inhalt</h4>
    <p>Bei dieser Webseite handelt es sich um ein rein fiktives Projekt ohne Kundenbezug.<br>
        Die Idee besteht darin, eine Plattform bereit zu stellen, welche das Verleihen von Gegenständen oder Anbieten von 
        Dienstleistungen in der Nachbarschaft ermöglicht. Dieser Austausch erfolgt ohne Bezahlung, da der Hilfsgedanke
        im Vordergrund steht. Stattdessen wird eine imaginäre Einheit namens "Kiez Punkte" eingeführt, um eine Vergleichbarkeit
        im Bezug auf die Wertigkeit der Inserate zu ermöglichen und ein Punktekonto für die Mitglieder realisieren zu können.<br>
        Der Interessent kann mit dem Anbieter Kontakt aufnehmen (per Mail oder telefonisch) und somit dessen Angebot annehmen.
        Im Gegenzug wird dem Interessenten die entsprechende Anzahl Kiez Punkte vom Punktekonto abgezogen und dem Anbieter wiederum
        gutgeschrieben.
    </p>
    
