    <h4 id="Design">Design</h4>
    
    <p>Um eine Überladung der Seite (wie bei Ebay-Kleinanzeigen) zu vermeiden, ist das Design bewusst schlicht gehalten. 
       Es wurde darauf geachtet einen einheitlichen Aspekt zu folgen. Hierzu finden sich vereinzelt auf den verschiedenen
       Seiten immer wieder grüne Elemente. Die Farbe Grün soll hierbei den Hilfsgedanken sowie das freundliche Miteinander
       in einer Nachbarschaft untermauern.<br>
       Zur Hervorhebung einzlner Meldungen, sowie des Navigationsbereiches kommt die modern wirkende Schriftart "Damion" zum Einsatz. Um den
       den Nutzer das aktuell ausgewählte Element im Menü zu visualisieren, wechselt erst bei Überfahren mit der Maus das Navigationselement
       in die Hauptschriftfarbe schwarz. Letztlich auch um die Navigationsleiste nicht in den Vordergrund zu setzen.
       Um für eine gute Lesbarkeit des Hauptbereiches zu sorgen, wird die serifenlose Schrift "Sans-Serif" eingesetzt.<br>
       Das Layout der Hauptseiten für die Inserate ist so gestaltet, dass die einzelnen Einträge in separaten Flex-Boxen (max. 2 nebeneinander)
       auf der linken Seite angezeigt werden. Auf der rechten Seite befindet sich ein fixierter Link auf das Formular zum
       Aufgeben eines neuen Inserates. Somit bleibt dieser auch beim Scrollen für den Benutzer immer ersichtlich.</p>
    
