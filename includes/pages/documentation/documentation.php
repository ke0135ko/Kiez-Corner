    <h4 id="Idea" >Idee</h4>
    <p>Bei dieser Webseite handelt es sich um ein rein fiktives Projekt zum Thema "Sharing Economy" ohne Kundenbezug.<br>
        Die Idee besteht darin, eine Plattform bereit zu stellen, welche das Verleihen von Gegenständen oder Anbieten von 
        Dienstleistungen in der Nachbarschaft ermöglicht. Dieser Austausch erfolgt ohne Bezahlung, da der Hilfsgedanke
        im Vordergrund steht. Stattdessen wird eine imaginäre Einheit namens "Kiez Punkte" eingeführt, um eine Vergleichbarkeit
        im Bezug auf die Wertigkeit der Inserate zu ermöglichen.
        Der Interessent kann mit dem Anbieter Kontakt aufnehmen (per Mail oder telefonisch) und somit dessen Angebot annehmen.<br>
        Im späteren Verlauf wäre auch die Integration eines Punktekontos denkbar. So könnten Angebote nur angenommen werden, wenn der
        Benutzer entsprechend viele Kiez Punkte hat. Im Gegenzug könnten den Inserenten Punkte gutgeschrieben werden.
    </p>
    
